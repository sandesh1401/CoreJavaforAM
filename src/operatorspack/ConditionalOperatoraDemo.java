package operatorspack;

public class ConditionalOperatoraDemo {

	public static void main(String[] args) {
		
		   // ?: it is Conditional operator
		
				int a = 5, b = 14, c;
				// based on condition like true and false example =5 is the greater than 14 are true or false is false then answer is trueside that's it
				c = a > b ? 9 : 19;
				
				System.out.println(c);  // 19
				
				int i = 7, j = 17;
				
				double k;
				
				k = i > j ? 1.2 : 2.3;
				
				System.out.println(k);  // 2.3

	}

}
