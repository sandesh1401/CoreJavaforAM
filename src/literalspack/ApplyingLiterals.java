package literalspack;

public class ApplyingLiterals {

	public static void main(String[] args) {
		//Information for understanding literals.
		
		System.out.println(9); // Integer 
		System.out.println(99); // Integer
		System.out.println(999); // Integer
		System.out.println(9999);  // Integer
		System.out.println(99L); // Long 
		System.out.println(999999999l); // Long literal
		System.out.println(1.23); // double literal
		System.out.println(1.23d); // double literal
		System.out.println(1.23D); // double literal
		System.out.println(1.23F); // floating point literal
		System.out.println(1.23f); // floating point literal
		System.out.println(true); // boolean
		System.out.println(false); // boolean
		System.out.println('9'); // character literal
		System.out.println('s'); // character literal
		System.out.println('$'); // character literal
		System.out.println("sandesh"); // String literal
		System.out.println("Sandesh Marathe"); // String literal
		System.out.println("9"); // String literal

		
		
		
	}

}
